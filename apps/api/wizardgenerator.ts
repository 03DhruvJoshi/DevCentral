function buildDockerComposeYaml(opts: Set<string>): string {
  let services = `services:\n  app:\n    build: .\n    ports:\n      - "3000:3000"\n    env_file: .env\n    depends_on:`;
  if (opts.has("postgres")) services += `\n      db:\n        condition: service_healthy`;
  if (opts.has("redis")) services += `\n      redis:\n        condition: service_started`;
  services += `\n    networks:\n      - app-network`;

  if (opts.has("postgres")) {
    services += `\n\n  db:\n    image: postgres:16-alpine\n    environment:\n      POSTGRES_DB: \${DB_NAME:-app}\n      POSTGRES_USER: \${DB_USER:-postgres}\n      POSTGRES_PASSWORD: \${DB_PASSWORD:-postgres}\n    volumes:\n      - postgres-data:/var/lib/postgresql/data\n    ports:\n      - "5432:5432"\n    healthcheck:\n      test: ["CMD-SHELL", "pg_isready -U \${DB_USER:-postgres}"]\n      interval: 10s\n      retries: 5\n    networks:\n      - app-network`;
  }
  if (opts.has("redis")) {
    services += `\n\n  redis:\n    image: redis:7-alpine\n    ports:\n      - "6379:6379"\n    volumes:\n      - redis-data:/data\n    networks:\n      - app-network`;
  }

  services += `\n\nvolumes:${opts.has("postgres") ? `\n  postgres-data:` : ""}${opts.has("redis") ? `\n  redis-data:` : ""}\n\nnetworks:\n  app-network:\n    driver: bridge`;

  const files = [
    { path: "docker-compose.yml", content: services },
    { path: "Dockerfile", content: `FROM node:20-alpine\nWORKDIR /app\nCOPY package*.json ./\nRUN npm ci\nCOPY . .\nEXPOSE 3000\nCMD ["node", "src/index.js"]\n` },
    { path: ".env.example", content: `NODE_ENV=development\nPORT=3000${opts.has("postgres") ? `\nDB_NAME=app\nDB_USER=postgres\nDB_PASSWORD=postgres\nDB_HOST=db` : ""}${opts.has("redis") ? `\nREDIS_URL=redis://redis:6379` : ""}\n` },
    { path: "README.md", content: `# {{projectName}}\n\nGenerated by DevCentral Scaffolder.\n\n## Getting Started\n\n\`\`\`bash\ncp .env.example .env\ndocker compose up -d\ndocker compose logs -f app\n\`\`\`\n` },
  ];

  return toYaml(files);
}

