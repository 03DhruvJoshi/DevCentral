// ── Dispatch ──────────────────────────────────────────────────────────────────

export function generateYaml(frameworkId: string, optionIds: string[]): string {
  const opts = new Set(optionIds);
  switch (frameworkId) {
    case "nextjs":           return buildNextjsYaml(opts);
    case "react-vite":       return buildReactViteYaml(opts);
    case "vue-nuxt":         return buildVueNuxtYaml(opts);
    case "sveltekit":        return buildSvelteKitYaml(opts);
    case "laravel":          return buildLaravelYaml(opts);
    case "django":           return buildDjangoYaml(opts);
    case "rails":            return buildRailsYaml(opts);
    case "expo":             return buildExpoYaml(opts);
    case "flutter":          return buildFlutterYaml(opts);
    case "swift-ios":        return buildSwiftIosYaml(opts);
    case "kotlin-android":   return buildKotlinAndroidYaml(opts);
    case "express":          return buildExpressYaml(opts);
    case "nestjs":           return buildNestJsYaml(opts);
    case "fastapi":          return buildFastApiYaml(opts);
    case "go-gin":           return buildGoGinYaml(opts);
    case "spring-boot":      return buildSpringBootYaml(opts);
    case "pytorch":          return buildPyTorchYaml(opts);
    case "langchain":        return buildLangChainYaml(opts);
    case "data-pipeline":    return buildDataPipelineYaml(opts);
    case "jupyter":          return buildJupyterYaml(opts);
    case "docker-compose":   return buildDockerComposeYaml(opts);
    case "github-actions":   return buildGithubActionsYaml();
    case "terraform-aws":    return buildTerraformAwsYaml(opts);
    case "kubernetes":       return buildKubernetesYaml(opts);
    default:
      return toYaml([{ path: "README.md", content: `# {{projectName}}\n\nFramework "${frameworkId}" is not yet supported.\n` }]);
  }
}
